public class Quarto
{
    private int id;
    private String situacao;

    public Quarto()
    {

    }

    public int getId()
    {
        return this.id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getSituacao()
    {
        return this.situacao;
    }

    public void setSituacao(String situacao)
    {
        this.situacao = situacao;
    }
}
